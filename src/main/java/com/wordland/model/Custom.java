package com.wordland.model;

public class Custom {
    private Integer id;

    private Integer accountId;

    private Integer customNum;

    private Integer customCatalog;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getAccountId() {
        return accountId;
    }

    public void setAccountId(Integer accountId) {
        this.accountId = accountId;
    }

    public Integer getCustomNum() {
        return customNum;
    }

    public void setCustomNum(Integer customNum) {
        this.customNum = customNum;
    }

    public Integer getCustomCatalog() {
        return customCatalog;
    }

    public void setCustomCatalog(Integer customCatalog) {
        this.customCatalog = customCatalog;
    }
}